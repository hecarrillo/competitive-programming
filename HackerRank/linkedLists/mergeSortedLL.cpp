/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode* next;
 * };
 *
 */
SinglyLinkedListNode* mergeLists(SinglyLinkedListNode* head1, SinglyLinkedListNode* head2) {
  SinglyLinkedListNode * p1 = head1;
  SinglyLinkedListNode * p2 = head2;

  if ((!p1 && !p2) || !p2) {
    return p1;
  } else if (!p1) return p2;

  SinglyLinkedListNode * ansHead;

  if (p1 -> data <= p2 -> data) {
    ansHead = p1;
    p1 = p1 -> next;
  } else {
    ansHead = p2;
    p2 = p2 -> next;
  }

  SinglyLinkedListNode * current = ansHead;

  while (p1 && p2) {
    if(p1 -> data <= p2-> data) {
      current -> next = p1;
      current = p1;
      p1 = p1 -> next;
    } else {
      current -> next = p2;
      current = p2;
      p2 = p2 -> next;
    }
  }

  current -> next = (p1)? p1 : p2;

  return ansHead;
}

